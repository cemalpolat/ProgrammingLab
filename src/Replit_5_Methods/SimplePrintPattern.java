package Replit_5_Methods;

public class SimplePrintPattern {
    public static void main(String[] args) {

        printHollowRect();
    }
    public static void printHollowRect(){

        for (int i = 1; i < 6; i++) {
                if (i ==1 || i == 5){
                    System.out.println("*****");
                    continue;
                }
            if (i==2 || i == 4 || i ==3){
                System.out.println("*   *");
                continue;
            }
            }
        }
    }





